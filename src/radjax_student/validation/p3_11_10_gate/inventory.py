from radjax_student.validation.p3_11_10_gate.models import GateCaseDefinition


def _cases(
    section: str,
    execution_class: str,
    positives: tuple[str, ...],
    negatives: tuple[str, ...],
) -> tuple[GateCaseDefinition, ...]:
    values: list[GateCaseDefinition] = []
    for slug in positives:
        values.append(
            GateCaseDefinition(
                f"{section}.positive.{slug}",
                section,
                execution_class,
                "pass",
                None,
                _BOUNDARIES[section],
                slug.replace("_", " "),
            )
        )
    for slug in negatives:
        case_id = f"{section}.reject.{slug}"
        values.append(
            GateCaseDefinition(
                case_id,
                section,
                execution_class,
                "reject",
                _expected_failure(section, slug),
                _BOUNDARIES[section],
                slug.replace("_", " "),
            )
        )
    return tuple(values)


